      SUBROUTINE USER10
      IMPLICIT REAL*8 (A-H, O-Z),  INTEGER*4 (I-N)
      INCLUDE  'blkcom.ins'
      INCLUDE  'space2.ins'
      IF ( IPRSUP  .GE.  1 )
     1 WRITE (LUNIT6, 1641)  BUS6, JWORD, OMEGA
 1641 FORMAT ( ' Top of USER10.  BUS6, JWORD, OMEGA =',  A6, I6, E13.5 )
      IF ( BUS6 .EQ. 'HYPERB' ) THEN
      D1 = 50. * TWOPI / OMEGA
      D2 = ONEHAF * ( D1 + 1.0 )
