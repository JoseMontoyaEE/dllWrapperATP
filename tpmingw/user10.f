      CALL STOPTP
 9800 IF ( IPRSUP .GE. 1 )
     1 WRITE (LUNIT6, 9814)  D1, D2
 9814 FORMAT ( ' Exit USER10.  D1, D2 =',  2E14.6  )
      RETURN
      END
